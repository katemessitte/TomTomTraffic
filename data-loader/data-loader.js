const fs = require('fs');
const path = require('path');

const sparseRoute = 10; // every ~300m, roughly 60 points
const sections = routeLatLon.filter((d, i) => i % sparseRoute === 0);

// Read API keys from .apikeys file — one key per line, file is gitignored
const apiKeysPath = path.join(__dirname, '.apikeys');
let apiKeysToTry = [];
try {
  apiKeysToTry = fs.readFileSync(apiKeysPath, 'utf8')
    .split('\n')
    .map(k => k.trim())
    .filter(k => k.length > 0);
} catch (err) {
  console.error(`ERROR: Could not read API keys file at ${apiKeysPath}`);
  console.error(`  Create the file with one TomTom API key per line.`);
  console.error(`  See .apikeys.example for the expected format.`);
  process.exit(1);
}
if (apiKeysToTry.length === 0) {
  console.error(`ERROR: ${apiKeysPath} exists but contains no keys.`);
  console.error(`  Add at least one TomTom API key (one per line).`);
  process.exit(1);
}

// Polling interval: set INTERVAL_MINUTES env variable or defaults to 60
const INTERVAL_MS = parseInt(process.env.INTERVAL_MINUTES || '60') * 60 * 1000;

async function getTrafficForOnePoint([lat, lon], key) {
  const params = new URLSearchParams({
    key,
    point: `${lat},${lon}`
  });

  const url =
    `https://api.tomtom.com/traffic/services/4/flowSegmentData/absolute/10/json?${params}`;

  const response = await fetch(url);

  if (!response.ok) {
    throw new Error(`TomTom flow error ${response.status} for ${lat},${lon}`);
  }

  const data = await response.json();
  return data.flowSegmentData;
}

// Try each API key in sequence; throws only when all keys are exhausted
async function getTrafficWithKeyRotation([lat, lon]) {
  for (const key of apiKeysToTry) {
    try {
      return await getTrafficForOnePoint([lat, lon], key);
    } catch (err) {
      console.warn(`Key ...${key.slice(-6)} failed for ${lat},${lon}: ${err.message}`);
    }
  }
  throw new Error(`All API keys failed for ${lat},${lon}`);
}

function summarizeFlow([lat, lon], flow) {
  const now = new Date();
  const current = flow.currentSpeed;
  const free = flow.freeFlowSpeed;
  const congestion = free > 0 ? 1 - current / free : 0;

  // Format hour as YYYY-MM-DD HH:00 using plain JS (no d3 dependency)
  const pad = n => String(n).padStart(2, '0');
  const hour = `${now.getFullYear()}-${pad(now.getMonth() + 1)}-${pad(now.getDate())} ${pad(now.getHours())}:00`;

  return {
    timestamp: now.toISOString(),
    hour,
    lat,
    lon,
    current_speed: current,
    free_flow_speed: free,
    congestion: congestion.toFixed(4),
    speed_ratio: free > 0 ? (current / free).toFixed(4) : '0'
  };
}

function rowsToCsv(rows) {
  if (rows.length === 0) return '';
  const headers = Object.keys(rows[0]);
  const lines = [
    headers.join(','),
    ...rows.map(r => headers.map(h => r[h]).join(','))
  ];
  return lines.join('\n') + '\n';
}

function fileTimestamp(date) {
  // Produces e.g. 2026-05-05T14-30-00 (safe for filenames on all OSes)
  return date.toISOString().slice(0, 19).replace(':', '-').replace(':', '-');
}

async function collect() {
  const now = new Date();
  console.log(`[${now.toISOString()}] Collecting traffic data for ${sections.length} route points...`);

  const outDir = path.join(__dirname, 'data');
  fs.mkdirSync(outDir, { recursive: true });

  let allFailed = true;
  const rows = [];

  for (const section of sections) {
    try {
      const flow = await getTrafficWithKeyRotation(section);
      rows.push(summarizeFlow(section, flow));
      allFailed = false;
    } catch (err) {
      console.error(`  Skipping ${section}: ${err.message}`);
    }
  }

  const suffix = allFailed ? '-error' : '';
  const filename = path.join(outDir, `${fileTimestamp(now)}${suffix}.csv`);
  fs.writeFileSync(filename, rowsToCsv(rows));
  console.log(`  Saved ${rows.length} rows → ${path.relative(process.cwd(), filename)}`);
}

console.log(`TomTom data loader started. Collecting every ${INTERVAL_MS / 60000} minutes.`);
console.log(`API keys loaded: ${apiKeysToTry.length}`);

collect(); // run once immediately, then on interval
setInterval(collect, INTERVAL_MS);

